def build_items(scene_name):
    bases = SCENE_BASES[scene_name]
    items = []
    for base in bases:
        board_suffix = "陈设板" if "组合" in base else "组合板"
        mat_suffix = "承托垫" if "分区" in base else "分区垫"
        items.extend((base + board_suffix, base + mat_suffix))
    if len(items) != 50 or len(set(items)) != 50:
        raise RuntimeError(f"{scene_name} generated invalid item count")
    return items


def format_block(scene_name):
    items = build_items(scene_name)
    lines = []
    for index in range(0, len(items), 5):
        chunk = items[index:index + 5]
        lines.append("            " + ", ".join(repr(item) for item in chunk) + ",")
    return "        'scene_expansion_items': [\n" + "\n".join(lines) + "\n        ],"


def main():
    text = TARGET.read_text(encoding="utf-8")
    pattern = re.compile(
        r"(?P<indent>        )'scene_expansion_items': \[\n(?P<body>.*?)(?P=indent)\],",
        re.S,
    )
    matches = list(pattern.finditer(text))
    if len(matches) != len(SCENE_BASES):
        raise RuntimeError(f"Expected {len(SCENE_BASES)} scene_expansion_items blocks, found {len(matches)}")

    parts = []
    last = 0
    for match, scene_name in zip(matches, SCENE_BASES):
        parts.append(text[last:match.start()])
        parts.append(format_block(scene_name))
        last = match.end()
    parts.append(text[last:])
    TARGET.write_text("".join(parts), encoding="utf-8")


if __name__ == "__main__":
    main()
